function getLength(arr) {
    return arr.length
}
function getFirst(arr) {
    return arr[0]
}
function getLast(arr) {
    const arrayLast = arr[arr.length - 1];
    return arrayLast
}
function pushElement(arr) {
    var el = 1;
    // arr.push(1);
    arr.push(el);
    return arr
}

function popElement(arr) {
    arr.pop(1);
    return arr
}